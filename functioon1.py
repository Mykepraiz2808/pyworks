def add(x, y):
    calc_add = x + y
    return calc_add
def sub(x, y):
    calc_sub = x - y
    return calc_sub
def mult(x, y):
    calc_mult = x * y
    return calc_mult
def div(x, y):
    calc_div = x / y
    return calc_div
def Raise(x,y):
    calc_Raise = x ** y
    return calc_Raise
    
